import xlrd  # Для чтения старых файлов .xls
import openpyxl  # Для чтения/записи новых файлов .xlsx

# Функция 1: загрузка данных из файла прошлого года
def download_ly(file_path):
    wb = xlrd.open_workbook(file_path)
    sh = wb.sheet_by_index(0)
    
    # Считываем заголовки из первой строки
    # Проходим по всем столбцам j от 0 до sh.ncols-1
    heading = [sh.cell_value(0, j) for j in range(sh.ncols)]
    
    lines = []  # Список для хранения всех строк данных
    
    # Проходим по всем строкам со второй
    for i in range(1, sh.nrows):
        line = {}  # Создаем пустой словарь для одной строки
        
        # Проходим по всем столбцам
        for j in range(len(heading)):
            # Заполняем словарь: 
            # ключ = название столбца
            # значение = данные из ячейки (строка i, столбец j)
            line[heading[j]] = sh.cell_value(i, j)
        
        # Добавляем готовую строку в общий список
        lines.append(line)
    
    # Возвращаем список словарей с данными
    return lines

# Функция 2: копирование ФИО
def task_5(lines_ly, ws_new, quant_disc, quant_type, quant_name):
    """
    Параметры:
    lines_ly - данные прошлого года (список словарей)
    ws_new - лист нового файла (объект Worksheet)
    quant_disc - номер столбца "Дисциплина" (нумерация с 1)
    quant_type - номер столбца "Вид нагрузки" (нумерация с 1)
    quant_name - номер столбца "ФИО" (нумерация с 1)
    
    Возвращает:
    количество заполненных строк
    """
    
    # Шаг 1: анализируем данные прошлого года
    # Создаем словарь для хранения преподавателей по парам (дисциплина, вид_нагрузки)
    teacher_key = {}
    
    # Перебираем все строки из данных прошлого года
    for s in lines_ly:
        
        # Извлекаем данные из строки, убираем лишние пробелы
        disc = str(s.get("Дисциплина", "")).strip()  # Название дисциплины
        load_type = str(s.get("Вид нагрузки", "")).strip()  # Вид нагрузки
        name = str(s.get("ФИО", "")).strip()  # ФИО преподавателя
        
        # Проверяем, что все данные заполнены
        # Если хоть что-то пустое или равно "None" - пропускаем строку
        if not disc or not load_type or not name or name == "None":
            continue  # Переходим к следующей строке
        
        # Создаем ключ из двух значений: (дисциплина, вид_нагрузки)
        # Это будет как "уникальный идентификатор" пары
        key = (disc, load_type)
        
        # Если такого ключа еще нет в словаре
        if key not in teacher_key:
            # Создаем пустое МНОЖЕСТВО (set) для хранения ФИО
            # Множество хранит только уникальные значения
            teacher_key[key] = set()
        
        # Добавляем ФИО в множество преподавателей для этой пары
        teacher_key[key].add(name)
    
    #Шаг 2: заполняем новый файл
    filled_in = 0  # Счетчик заполненных строк
    
    # Проходим по всем строкам нового файла
    # Начинаем со строки 2, так как строка 1 - это заголовки
    # ws_new.max_row - общее количество строк в листе
    # range(2, max_row + 1) - от 2 до последней строки включительно
    for i in range(2, ws_new.max_row + 1):
        
        # Читаем дисциплину из текущей строки нового файла
        # quant_disc - номер столбца с дисциплиной
        # .value - берем значение из ячейки
        # or "" - если значение None, подставляем пустую строку
        # .strip() - убираем лишние пробелы
        disc_new = str(ws_new.cell(i, quant_disc).value or "").strip()
        
        # Читаем вид нагрузки из текущей строки нового файла
        # quant_type - номер столбца с видом нагрузки
        load_type_new = str(ws_new.cell(i, quant_type).value or "").strip()
        
        # Если дисциплина или вид нагрузки пустые - пропускаем строку
        if not disc_new or not load_type_new:
            continue
        
        # Создаем ключ для поиска в данных прошлого года
        key_new = (disc_new, load_type_new)
        
        # Проверяем, есть ли такая пара (дисциплина, вид_нагрузки) в прошлом году
        if key_new not in teacher_key:
            continue  # Если нет - переходим к следующей строке
        
        # Получаем множество преподавателей для этой пары
        teachers = teacher_key[key_new]
        
        # Копируем ФИО только если в прошлом году был один преподаватель
        if len(teachers) == 1:
            
            # Преобразуем множество в список и берем первый элемент
            name_one = list(teachers)[0]
            
            # Записываем ФИО в новый файл
            # quant_name - номер столбца с ФИО
            ws_new.cell(i, quant_name).value = name_one
            
            # Увеличиваем счетчик заполненных строк
            filled_in += 1
    
    # Возвращаем количество строк, в которые было скопировано ФИО
    return filled_in


#===================================================================
#Задаем имена файлов
file_ly = "15_вариант.xls"  # Файл с данными прошлого года 
file_new = "нагрузка-вар2-ТиСАПРМП-2023-2024.xlsx"  # Новый файл
file_result = "задача5_результат.xlsx"  # Имя файла для сохранения результата

#Загружаем данные прошлого года
print('Чтение данных прошлого года...')
lines_ly = download_ly(file_ly)  # Вызываем функцию загрузки
print(f'Загружено строк: {len(lines_ly)}')  # Выводим количество загруженных строк

#Открываем новый файл
print('Открытие файла нового года...')
wb_new = openpyxl.load_workbook(file_new)  # Открываем Excel-файл
ws_new = wb_new["Нагрузка"]  # Берем лист с названием "Нагрузка"

#Определяем номера нужных столбцов
heading_new = [
    ws_new.cell(1, j + 1).value for j in range(ws_new.max_column)
]

# Ищем в заголовках нужные названия столбцов
try:
    # .index() ищет элемент в списке и возвращает его позицию
    # +1 потому что в Excel нумерация с 1, а в списке с 0
    quant_disc = heading_new.index("Дисциплина") + 1
    quant_type = heading_new.index("Вид нагрузки") + 1
    quant_name = heading_new.index("ФИО") + 1
except ValueError as ve:  # Если столбец не найден - вылетит ошибка ValueError
    print(f'ОШИБКА - не найден столбец - {ve}')

# Выводим номера столбцов для проверки
print(f'Столбцы: "Дисциплина" = {quant_disc}, "Вид нагрузки" = {quant_type}, "ФИО" = {quant_name}')

print('Выполнение задачи...')
# Вызываем функцию с правильным порядком аргументов
n = task_5(lines_ly, ws_new, quant_disc, quant_type, quant_name)
print(f'Заполнено строк: {n}')  # Выводим результат

# Сохраняем изменения в новый файл
wb_new.save(file_result)
print(f'Работа выполнена!')
print(f'Ознакомиться с результатом можно в файле: {file_result}')